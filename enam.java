public class enam {
    public static void main(String[] args) {
        int awal=1;
        int akhir=10;
        int jumlah=0;
        System.out.println("penjumlahan deret dari 1 sampai 10");
        while(awal<=akhir){
            jumlah=jumlah+awal;
            awal++;
        }
        System.out.println("Jumlahnya " +jumlah);
    }
}
